export function fillTemplate(line, name) {
  return line.replaceAll("{name}", name);
}

export function pickAmbient(rng = Math.random) {
  return AMBIENT[Math.floor(rng() * AMBIENT.length)];
}

// Returns a milestone line for `level`, or null if that level has no event.
export function eventLineFor(level, rng = Math.random) {
  const pool = EVENTS[level];
  if (!pool) return null;
  return pool[Math.floor(rng() * pool.length)];
}

// Milestone levels newly crossed when going from `before` to `after` (exclusive..inclusive).
export function milestonesCrossed(before, after) {
  const out = [];
  for (let lv = before + 1; lv <= after; lv++) {
    if (EVENTS[lv]) out.push(lv);
  }
  return out;
}
